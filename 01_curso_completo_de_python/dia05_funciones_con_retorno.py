# FUNCION CON RETORNO DE VALORES
def saludar():
  return "Buenos dias"
def saludoConReturn():
  return print("Buenos dias desde el return")
saludar()  # por consola no se ve nada, lo que hace la funcion es retornarme el string "Buenos dias"
print(saludar()) # Buenos dias
saludoConReturn() # Buenos dias desde el return